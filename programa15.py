#operadores aritmeticos
x=7.0
y=3.0
a=x+y #suma
b=x-y #resta
c=x*y #multiplication
d=x/y #division
e=x**y #potencia
f=x//y #division redondeada
print(f'x={x}, y={y}\n') #\n significa salto de linea
print(f'suma={a}')
print(f'resta={b}')
print(f'multiplicacion={c}')
print(f'division={d}')
print(f'potencia={e}')
print(f'division redondeada={f}')